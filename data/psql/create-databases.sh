#!/bin/bash
set -e

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
	CREATE DATABASE orders;
	CREATE DATABASE payment;
	CREATE DATABASE product;
	GRANT ALL PRIVILEGES ON DATABASE orders TO admin;
	GRANT ALL PRIVILEGES ON DATABASE payment TO admin;
	GRANT ALL PRIVILEGES ON DATABASE product TO admin;
EOSQL